# 1. Integer variable age
age = 25
print(age)

# 2. Float variable temperature
temperature = 98.6
print(temperature)

# 3. Complex number variable complex_num
complex_num = 3 + 4j
print(complex_num)

# 4. Boolean variable is_student
is_student = True
print(is_student)

# 5. Boolean variable has_job
has_job = False
print(has_job)

# 6. String variable greeting with single quotes
greeting = 'Hello'
print(greeting)

# 7. String variable city with double quotes
city = "New York"
print(city)

# 8. String variable quote with triple single quotes
quote = '''Life is short'''
print(quote)

# 9. Multi-line string poem with triple double quotes
poem = """Roses are red,
Violets are blue."""
print(poem)

# 10. Three variables: score, height, name
score = 95
height = 5.9
name = "Alice"

print(score)
print(height)
print(name)
